package ch8;

public class Birthday {
	private int day;
	private int month;
	private int year;
	// getter and setter 는 데이터 가져오고 집어넣을 때쓴다. 수정할떄도 쓴다 .
	 public int getDay() {
		 return day;
	 }
	public void setDay(int day) {
		this.day =day;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	
	
}
